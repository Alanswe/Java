public class cambioValorEnVariable {
    public static void main(String[] args) {
        
        int a = -10;
        int b = -a;/*- - = + por lo tanto cambia de negativo a positivo*/
        int c = a + 1;
        System.out.println("Resultado B: "+ b);
        System.out.println("Resultado C: "+ c);


    }
}
